SKIP_PATTERNS = (
    re.compile(r"^```"),
    re.compile(r"^https?://"),
    re.compile(r"^POST /"),
    re.compile(r"^GET /"),
    re.compile(r"^PUT /"),
    re.compile(r"^DELETE /"),
    re.compile(r"^PATCH /"),
)


def fix_en_links(text: str) -> str:
    """Point English doc links to *.md, not *_id.md."""
    return re.sub(r"\]\(([^)]*)_id\.md\)", r"](\1.md)", text)


def translate_line(line: str) -> str:
    if any(p.match(line) for p in SKIP_PATTERNS):
        return line
    out = line
    for src, dst in REPLACEMENTS:
        out = out.replace(src, dst)
    return out


def translate_file(id_path: Path, en_path: Path) -> None:
    text = id_path.read_text(encoding="utf-8")
    lines = [translate_line(ln) for ln in text.splitlines(keepends=True)]
    en_path.write_text(fix_en_links("".join(lines)), encoding="utf-8")


def discover_targets(root: Path) -> list[Path]:
    if len(sys.argv) > 1:
        return [Path(p) for p in sys.argv[1:]]
    patterns = [
        "sequences/[0-9]*_id.md",
        "migration/backend-modules_id.md",
    ]
    targets: list[Path] = []
    for pat in patterns:
        targets.extend(sorted(root.glob(pat)))
    return targets


def main() -> int:
    root = Path(__file__).resolve().parents[1]
    for id_path in discover_targets(root):
        if not id_path.name.endswith("_id.md"):
            continue
        en_path = id_path.with_name(id_path.name.replace("_id.md", ".md"))
        translate_file(id_path, en_path)
        print(en_path)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
